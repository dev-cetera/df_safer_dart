typedef ResultOption<T extends Object> = Result<Option<T>>;

extension ResultOptionX<T extends Object> on ResultOption<T> {
  @pragma('vm:prefer-inline')
  Result<T> unwrapResult() => unwrap().asResult();

  OptionResult<T> swap() {
    if (isErr()) {
      return const None();
    }
    return unwrap().map((e) => Ok(e));
  }
}
