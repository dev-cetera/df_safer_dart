class PanicIf {
  final bool condition;
  final String message;

  PanicIf(this.condition, [this.message = '']) {
    validate();
  }

  void validate() {
    if (condition) {
      throw Panic(this);
    }
  }
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

final class Panic {
  final PanicIf validate;

  const Panic(this.validate);

  @override
  String toString() {
    return '[Panic]: ${validate.message.isNotEmpty ? validate.message : 'A critical condition was not met!'}';
  }
}
