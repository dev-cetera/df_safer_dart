extension SaferMap1<K, V> on Map<K, V> {
  Option<Map<K, V>> get noneIfEmpty {
    return Option.from(isEmpty ? null : this);
  }
}

extension SaferMap2<K, V extends Object> on Map<K, V> {
  Option<V> getOption(K key) {
    if (containsKey(key)) {
      return Some(this[key]!);
    }
    return const None();
  }
}
